# general.variable.basic variables & how to assign

## variable type
### general
name = "kitten"                            # variable type: str
kind = 'cat'                               # variable type: str
age = 3                                    # variable type: int
weight = 5                                 # variable type: int
alive = True                               # variable type: bool
kitties = 3                                # variable type: int
kittiesName = ["Google", "Dot", "Com"]     # variable type: list

### assigning multiple values
kitty1, kitty2, kitty3 = kittiesName
kitty1, kitty2, kitty3 = ["Google", "Dot", "Com"]
kitty1Age, kitty2Age, kitty3Age = 1
